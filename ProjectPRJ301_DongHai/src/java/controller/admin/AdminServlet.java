    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        MovieDAO dal = new MovieDAO();
        userDAO dao = new userDAO();
        int totalMovie = dal.statisticMovie();
        int totalUser = dao.statisticUser();
        int totalView = dal.statisticView();
        Map<String, Integer> chart_1 = dal.chart1();
        String chartmovie = "";
        for (String entry : chart_1.keySet()) {
            chartmovie += entry + "=" + chart_1.get(entry) + ",";
        }
        Map<String, Integer> chart_2 = dal.chart2();
        String chartView = "";
        for (String entry : chart_2.keySet()) {
            chartView += entry + "=" + chart_2.get(entry) + ",";
        }
        chartmovie = chartmovie.substring(0, chartmovie.length() - 1);
        chartView = chartView.substring(0, chartView.length() - 1);
        request.setAttribute("chart1", chartmovie);
        request.setAttribute("chart2", chartView);
        request.setAttribute("totalMovie", totalMovie);
        request.setAttribute("totalUser", totalUser);
        request.setAttribute("totalView", totalView);
        request.getRequestDispatcher("admin-chart.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

